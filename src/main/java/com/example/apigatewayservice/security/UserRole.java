package com.example.apigatewayservice.security;

import com.google.common.collect.Sets;
import org.springframework.security.core.authority.SimpleGrantedAuthority;

import java.util.Set;
import java.util.stream.Collectors;

import static com.example.apigatewayservice.security.UserPermission.*;


public enum  UserRole {
    WAREHOUSEKEEPER(Sets.newHashSet(MAKE_ORDERS,SEE_CHANGES, ADD_STOCK)),
    MANAGER(Sets.newHashSet(SEE_ORDERS, SEE_CHANGES,SEE_STOCK));

    private final Set<UserPermission> permissions;

    UserRole(Set<UserPermission> permissions) {
        this.permissions = permissions;
    }

    public Set<UserPermission> getPermissions() {
        return permissions;
    }

    public Set<SimpleGrantedAuthority> getGrantedAuthority(){
        Set<SimpleGrantedAuthority> permissions = getPermissions().stream()
                .map(permission -> new SimpleGrantedAuthority(permission.getPermission()))
                .collect(Collectors.toSet());
        permissions.add(new SimpleGrantedAuthority("ROLE_" + this.name()));
        return permissions;
    }
}
